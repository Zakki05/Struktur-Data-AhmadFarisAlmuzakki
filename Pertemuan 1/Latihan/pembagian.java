public class pembagian{
	public static void main(String[] args){
		int a,b; //terdapat 2 variabel bertipe data integer
		float c; //terdapat 1 variabel bertipe data float
		a=10; //variabel a bernilai 10
		b=3; //variabel b bernilai 3
		c = (float) a / b; //c proses pembagian variabel a dan b
		System.out.println("a / b = " + c);
		System.out.println(a + " / " + b + " = " + c);
	}
}